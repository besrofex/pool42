#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i ;
	int		j ;
	char	str[5];

	i = 0 ;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			str[0] = (i / 10) + '0';
			str[1] = (i % 10) + '0';
			str[2] = ' ';
			str[3] = (j / 10) + '0';
			str[4] = (j % 10) + '0';
			write(1, str, 5);
			if (i != 98)
			{
				write(1, ", ", 2);
			}
			j++;
		}
		i++;
	}
}
